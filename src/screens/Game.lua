local Screen = require('lib/screens/Screen');
local Arena = require('src/game/Arena');

-- ------------------------------------------------
-- Module
-- ------------------------------------------------

local Game = {};

-- ------------------------------------------------
-- Constructor
-- ------------------------------------------------

function Game.new()
    local self = Screen.new();

    local arena;

    function self:init()
        arena = Arena.new();
        arena:init();
    end

    function self:update(dt)
        arena:update(dt);
    end

    function self:draw()
        arena:draw();
    end

    return self;
end

-- ------------------------------------------------
-- Return Module
-- ------------------------------------------------

return Game;

